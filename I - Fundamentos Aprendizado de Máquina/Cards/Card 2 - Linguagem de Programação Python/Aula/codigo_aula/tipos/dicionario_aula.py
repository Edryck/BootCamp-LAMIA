print(type(aluno))
print(aluno ['nome']) # Imprime o nome que está no dict
print(aluno['nota']) # Imprime a nota que está no dict
print(aluno['ativo']) # Imprime o status que está no dict
print(len(aluno)) # Imprime quantas chaves tem no dict
