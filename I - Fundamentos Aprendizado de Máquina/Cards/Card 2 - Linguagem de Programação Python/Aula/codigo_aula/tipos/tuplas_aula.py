# Cria uma tupla com nomes
nomes = ('Ana', 'Bia', 'Gui', 'Ana')
# Imprime o resultado se o elemento está na tupla, true ou false
print('Bia' in nomes)
# Imprime o nome no indice 0
print(nomes[0])
print(nomes [1:3]) # Imprime os elementos do 2 ao 3, não inclui o 4 
print(nomes [1:-1]) # Imprime os elementos sem incluir o penúltimo e o último
print(nomes [2:]) # Imprime todos apartir do 3
print(nomes [:-2])  # Imprime todos sem incluir o penúltimo
print(len(nomes)) # Imprime o tamanho da tupla
print(type(nomes)) # Imprime o tipo, nesse caso tuple
print(nomes) # Imprime a tupla inteira
